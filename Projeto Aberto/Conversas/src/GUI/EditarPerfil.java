    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (salvarPerfil.le() == null) {
            JOptionPane.showMessageDialog(this, "Nenhum perfil logado.");
            return;
        }
        try {
            Pessoa pessoa = salvarPerfil.le();
            pessoa.setNome(txtNome.getText());
            if (txtSexo.getText().isEmpty()) {
                pessoa.setSexo(' ');
            } else {
                pessoa.setSexo(txtSexo.getText().charAt(0));
            }
            if (Data.getSelectedDate() != null) {
                pessoa.setDataNasc(Data.getSelectedDate().getTime());
            }
            pessoa.setNacionalidade_Pessoa(txtNacionalidade.getText());

            PessoaDAO dao = new PessoaDAO();
            dao.atualizar(pessoa);

            salvarPerfil.grava(pessoa.getID_pessoa());
            JOptionPane.showMessageDialog(this, "Perfil atualizado com sucesso!");
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

