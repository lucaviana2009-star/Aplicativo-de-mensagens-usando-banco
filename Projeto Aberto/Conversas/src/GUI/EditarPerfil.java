    private void preencher() {
        if (salvarPerfil.le() == null) {
            JOptionPane.showMessageDialog(this,
                    "Você precisa entrar em um perfil antes de editar.");
            return;
        }
        Pessoa p = salvarPerfil.le();
        txtNome.setText(p.getNome());
        txtSexo.setText(String.valueOf(p.getSexo()));
        if (p.getDataNasc() != null) {
            Calendar cal = Calendar.getInstance();
            cal.setTime(p.getDataNasc());
            Data.setSelectedDate(cal);
        }
        txtNacionalidade.setText(p.getNacionalidade_Pessoa());
    }

