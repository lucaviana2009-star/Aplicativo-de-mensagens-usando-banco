    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        if (salvarPerfil.le() == null || meuFone == 0) {
            JOptionPane.showMessageDialog(this, "Você precisa de um perfil com telefone para enviar.");
            return;
        }
        Contato c = getContatoSelecionado();
        if (c == null) {
            JOptionPane.showMessageDialog(this, "Escolha um contato para conversar.");
            return;
        }
        if (txtMensagem.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite uma mensagem.");
            return;
        }
        try {
            Mensagens mensagem = new Mensagens();
            mensagem.setMensagem(txtMensagem.getText());
            mensagem.setID_remetente(meuFone);
            mensagem.setID_destinatario(c.getId_telefone());

            new MensagensDAO().inserir(mensagem);
            txtMensagem.setText("");
            mostrarConversa();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao enviar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

