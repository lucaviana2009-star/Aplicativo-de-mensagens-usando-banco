    public CadastroMensagem() {
        initComponents();
        if (salvarPerfil.le() == null) {
            JOptionPane.showMessageDialog(this,
                    "Você precisa entrar em um perfil antes de conversar.\n"
                    + "(Controle > Perfil > Entrar > Entrar em Existente)");
            return;
        }
        lblDe.setText("De: " + salvarPerfil.le().getNome());
        Telefone meu = new TelefoneDAO().procurarPorPessoa(salvarPerfil.le().getID_pessoa());
        meuFone = meu.getID_fone();
        if (meuFone == 0) {
            JOptionPane.showMessageDialog(this,
                    "Seu perfil não tem telefone cadastrado, então não dá para enviar mensagens.");
        }
        carregarContatos();
    }

    private void carregarContatos() {
        contatos = new ContatoDAO().listarPorPessoa(salvarPerfil.le().getID_pessoa());
        javax.swing.DefaultComboBoxModel<String> model = new javax.swing.DefaultComboBoxModel<>();
        for (Object o : contatos) {
            Contato c = (Contato) o;
            model.addElement(c.getNomeContato() + " (" + c.getFoneContato() + ")");
        }
        comboContato.setModel(model);
        mostrarConversa();
    }

    private void mostrarConversa() {
        areaConversa.setText("");
        Contato c = getContatoSelecionado();
        if (c == null || meuFone == 0) {
            return;
        }
        ArrayList msgs = new MensagensDAO().listarConversa(meuFone, c.getId_telefone());
        for (Object o : msgs) {
            Mensagens m = (Mensagens) o;
            String quem = (m.getID_remetente() == meuFone) ? "Eu" : c.getNomeContato();
            areaConversa.append(quem + ": " + m.getMensagem() + "\n");
        }
    }

    private Contato getContatoSelecionado() {
        int i = comboContato.getSelectedIndex();
        if (i < 0 || i >= contatos.size()) {
            return null;
        }
        return (Contato) contatos.get(i);
    }

