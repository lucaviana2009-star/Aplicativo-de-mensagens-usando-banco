    private void carregarContatos() {
        if (salvarPerfil.le() == null) {
            return;
        }
        ContatoDAO dao = new ContatoDAO();
        contatos = dao.listarPorPessoa(salvarPerfil.le().getID_pessoa());
        javax.swing.DefaultListModel<String> model = new javax.swing.DefaultListModel<>();
        for (Object o : contatos) {
            Contato c = (Contato) o;
            model.addElement(c.getNomeContato() + " (" + c.getFoneContato() + ")");
        }
        listaContatos.setModel(model);
    }

