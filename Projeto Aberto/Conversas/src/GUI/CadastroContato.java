    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        if (salvarPerfil.le() == null) {
            JOptionPane.showMessageDialog(this, "Entre em um perfil primeiro.");
            return;
        }
        String ddd = txtDDD.getText().trim();
        String numero = txtFone.getText().trim();
        if (ddd.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe o DDI.");
            return;
        }
        if (numero.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um número de telefone.");
            return;
        }
        int dddNumero;
        try {
            dddNumero = Integer.parseInt(ddd);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "DDI deve ser um número (ex.: 11).");
            return;
        }
        try {
            TelefoneDAO telefoneDAO = new TelefoneDAO();
            Telefone telefone = telefoneDAO.procurarPorNumero(numero, dddNumero);

            if (telefone.getID_fone() == 0) {
                JOptionPane.showMessageDialog(this, "Esse número não está cadastrado");
                return;
            }
            if (telefone.getID_pessoa() == salvarPerfil.le().getID_pessoa()) {
                JOptionPane.showMessageDialog(this, "Você não pode adicionar o seu próprio número.");
                return;
            }

            ContatoDAO contatoDAO = new ContatoDAO();
            if (contatoDAO.existe(salvarPerfil.le().getID_pessoa(), telefone.getID_fone())) {
                JOptionPane.showMessageDialog(this, "Esse contato já está na sua lista");
                return;
            }

            Contato contato = new Contato();
            contato.setId_pessoa(salvarPerfil.le().getID_pessoa());
            contato.setId_telefone(telefone.getID_fone());
            contatoDAO.inserir(contato);

            Telefone meuTelefone = telefoneDAO.procurarPorPessoa(salvarPerfil.le().getID_pessoa());
            if (meuTelefone.getID_fone() == 0) {
                JOptionPane.showMessageDialog(this,
                        "Contato adicionado só para você. Como você não tem telefone cadastrado, "
                        + "a outra pessoa não pôde te adicionar de volta.");
            } else {
                if (!contatoDAO.existe(telefone.getID_pessoa(), meuTelefone.getID_fone())) {
                    Contato contatoVolta = new Contato();
                    contatoVolta.setId_pessoa(telefone.getID_pessoa());
                    contatoVolta.setId_telefone(meuTelefone.getID_fone());
                    contatoDAO.inserir(contatoVolta);
                }
                JOptionPane.showMessageDialog(this, "Contato adicionado nos dois lados!");
            }

            txtDDD.setText("");
            txtFone.setText("");
            carregarContatos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao adicionar contato: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Contato c = getSelecionado();
        if (c == null) {
            return;
        }
        String novoDDD = JOptionPane.showInputDialog(this, "Novo DDI:");
        if (novoDDD == null || novoDDD.trim().isEmpty()) {
            return;
        }
        String novoNumero = JOptionPane.showInputDialog(this, "Novo número:");
        if (novoNumero == null || novoNumero.trim().isEmpty()) {
            return;
        }
        int dddNumero;
        try {
            dddNumero = Integer.parseInt(novoDDD.trim());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "DDI deve ser um número (ex.: 11).");
            return;
        }
        try {
            Telefone telefone = new TelefoneDAO().procurarPorNumero(novoNumero.trim(), dddNumero);
            if (telefone.getID_fone() == 0) {
                JOptionPane.showMessageDialog(this, "Esse número não está cadastrado no app.");
                return;
            }
            if (telefone.getID_pessoa() == salvarPerfil.le().getID_pessoa()) {
                JOptionPane.showMessageDialog(this, "Você não pode usar o seu próprio número.");
                return;
            }
            c.setId_telefone(telefone.getID_fone());
            new ContatoDAO().atualizar(c);
            JOptionPane.showMessageDialog(this, "Contato atualizado!");
            carregarContatos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao editar contato: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Contato c = getSelecionado();
        if (c == null) {
            return;
        }
        int resposta = JOptionPane.showConfirmDialog(this,
                "Excluir o contato \"" + c.getNomeContato() + "\"?",
                "Confirmar exclusão", JOptionPane.YES_NO_OPTION);
        if (resposta != JOptionPane.YES_OPTION) {
            return;
        }
        new ContatoDAO().excluir(c.getId_contato());
        JOptionPane.showMessageDialog(this, "Contato excluído!");
        carregarContatos();
    }//GEN-LAST:event_btnExcluirActionPerformed

    private Contato getSelecionado() {
        int i = listaContatos.getSelectedIndex();
        if (i < 0 || i >= contatos.size()) {
            JOptionPane.showMessageDialog(this, "Selecione um contato na lista.");
            return null;
        }
        return (Contato) contatos.get(i);
    }

